/* Faça um programa que, dado um valor definido numa constante, 
retorne "positive" se esse valor for positivo, "negative" se for 
negativo e "zero" caso contrário. */

const number = -1;


if (number > 0) {
    console.log('Positivo');
} else if (number < 0) {
    console.log('Negativo');
} else if (number == 0) {
    console.log('Zero');
}